app.get('/api/departments', (req, res) => {
    const sql = `SELECT employees AS employee, department.department FROM departments LEFT JOIN movies ON department.employees_id = employees.id ORDER BY employees.employee_name;`;
    db.query(sql, (err, rows) => {
        if (err) {
            res.status(500).json({ error: err.message });
            return;
        }
        res.json({
            message: 'success',
            data: rows
        });
    });
});

// Update salary
app.put('/api/salary/:id', (req, res) => {
    const sql = `UPDATE salary SET salary = ? WHERE id = ?`;
    const params = [req.body.salary, req.params.id];

    db.query(sql, params, (err, result) => {
        if (err) {
            res.status(400).json({ error: err.message });
        } else if (!result.affectedRows) {
            res.json({
                message: 'Salary not found'
            });
        } else {
            res.json({
                message: 'success',
                data: req.body,
                changes: result.affectedRows
            });
        }
    });
});










//this needs to be last
app.listen(PORT, () => {
    console.log(`Server running on port ${PORT}`);
});

